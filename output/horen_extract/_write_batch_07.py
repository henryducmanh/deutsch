def main():
    # Dedupe within batch by (wort, wortart) — keep first occurrence
    seen = set()
    deduped = []
    skipped_dup = 0
    for row in ROWS:
        folder, wort, wortart = row[0], row[1], row[2]
        key = (wort.lower(), wortart)
        if key in seen:
            skipped_dup += 1
            continue
        seen.add(key)
        deduped.append(row)

    # Write CSV
    with open(CSV_PATH, "w", encoding="utf-8", newline="") as f:
        writer = csv.writer(f, quoting=csv.QUOTE_MINIMAL)
        for row in deduped:
            writer.writerow(row)

    # Write JSON
    with open(JSON_PATH, "w", encoding="utf-8") as f:
        json.dump(FOLDERS, f, ensure_ascii=False, indent=2)

    # Report
    by_wortart = {}
    by_folder = {}
    for row in deduped:
        wa = row[2]
        by_wortart[wa] = by_wortart.get(wa, 0) + 1
        f0 = row[0]
        by_folder[f0] = by_folder.get(f0, 0) + 1

    print(f"Total rows written: {len(deduped)}")
    print(f"Folders processed: {len(FOLDERS)}")
    print(f"Dedupe skipped (within batch): {skipped_dup}")
    print(f"\nBy wortart:")
    for wa, n in sorted(by_wortart.items(), key=lambda x: -x[1]):
        print(f"  {wa}: {n}")
    print(f"\nBy folder (count):")
    zeros = [f for f in FOLDERS if f not in by_folder]
    if zeros:
        print(f"  Folders with 0 rows: {zeros}")
    else:
        print("  All folders have >=1 row")
    print(f"\nFiles written:")
    print(f"  {CSV_PATH}")
    print(f"  {JSON_PATH}")


if __name__ == "__main__":
    main()
